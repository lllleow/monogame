namespace MonoGame;

/// <summary>
/// Represents the type of texture for a tile.
/// </summary>
public enum TileTextureType
{
    /// <summary>
    /// Contains only a single texture
    /// </summary>
    Basic,
    /// <summary>
    /// Uses de simple connecting tileset
    /// </summary>
    SimpleConnecting,
    /// <summary>
    /// Uses de complete connecting tileset
    /// </summary>
    CompleteConnecting
}
